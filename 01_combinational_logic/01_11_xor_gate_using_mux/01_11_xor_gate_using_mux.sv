module mux
(
  input  d0, d1,
  input  sel,
  output y
);

  assign y = sel ? d1 : d0;

endmodule

//----------------------------------------------------------------------------
// Task
//----------------------------------------------------------------------------

module xor_gate_using_mux
(
    input  a,
    input  b,
    output o
);

  mux muxgate (
    .d0 ( b),
    .d1 (~b),
    .sel( a),
    .y  ( o)
  );


endmodule
