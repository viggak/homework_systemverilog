(
  input  [3:0] d0, d1, d2, d3,
  input  [1:0] sel,
  output [3:0] y
);

  logic [3:0] y_1, y_2;
    
  mux_2_1 mux_gate_1 (
    .d0 (d0    ),
    .d1 (d1    ),
    .sel(sel[0]),
    .y  (y_1   )
  );

  mux_2_1 mux_gate_2 (
    .d0 (d2    ),
    .d1 (d3    ),
    .sel(sel[0]),
    .y  (y_2   )
  );

  mux_2_1 mux_gate_3 (
    .d0 (y_1   ),
    .d1 (y_2   ),
    .sel(sel[1]),
    .y  (y     )
  );

endmodule
